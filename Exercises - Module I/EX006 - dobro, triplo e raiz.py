#Crie um algorítmo que leia um número e mostre o seu dobro e triplo e aiz quadrada. 

num = float(input('Digite um número: '))

print('O número digitado é: {}' . format(num))
print('O seu dobro é: {}' .format(num*2))
print('O seu triplo é: {}' .format(num*3))
print('Sua raiz quadrada é: {}' .format(num**0.5))